package exerLogicaProgramacao;

public class TiposPrimitivos {

	public static void main(String[] args) {
		// Todo são tipos primitivos 
		int a = 0;  // 32 bits - números inteiros  
		double b = 0; // 64 bits  números real(fracionado) ou inteiros
		short f = 32;   // 16 bits	
		long g = 89L;   // 64 bits
		float h = 77f; // 32 bits
		byte d = 21; // 8 bits 
		char e = 'T'; // 16 bits
		boolean c = true; // 1 bit
		
		
		
		
	}

}
